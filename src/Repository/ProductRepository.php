    /**
     * 减库存
     *
     * @param int $id 商品id
     * @return mixed
     */
    public function decrStock(int $id)
    {
        $qb = $this->createQueryBuilder('p');

        return $qb->update()
            ->set('p.stock', $qb->expr()->diff('p.stock', 1))
            ->where('p.id = :id')
            ->andWhere($qb->expr()->gt('p.stock', 0))
            ->setParameters([
                'id' => $id
            ])
            ->getQuery()
            ->execute();
    }
